package no.uio.medisin.bag.core.tree;

/**
 *
 * @author sr
 */
public class EvolutionaryDistance {

  private int i;
  private int j;
  private double dist;

  /**
   * @return the i
   */
  public int getI() {
    return i;
  }

  /**
   * @param i the i to set
   */
  public void setI(int i) {
    this.i = i;
  }

  /**
   * @return the j
   */
  public int getJ() {
    return j;
  }

  /**
   * @param j the j to set
   */
  public void setJ(int j) {
    this.j = j;
  }

  /**
   * @return the dist
   */
  public double getDist() {
    return dist;
  }

  /**
   * @param dist the dist to set
   */
  public void setDist(double dist) {
    this.dist = dist;
  }

}
